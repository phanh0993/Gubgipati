    content += `--------------------------------\n`;
    content += `T·ªîNG C·ªòNG: ${total.toLocaleString('vi-VN')}ƒë\n`;
    content += `================================\n`;
    content += `    C·∫£m ∆°n qu√Ω kh√°ch!\n`;
    content += `\n\n\n`;
    
    // T·∫°o file t·∫°m v√† in v·ªõi encoding UTF-8 v√† settings cho POS-80C
